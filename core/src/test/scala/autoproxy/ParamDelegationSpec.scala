package autoproxy

import org.scalatest.FunSuite

class ParamDelegationSpec extends FunSuite {
  class Bippy {
    def bippy(i : Int) : String = i.toString
  }


  test("can delegate to a parameter") {
    @delegating class Wibble(@proxy b: Bippy)

    val wibble = new Wibble(new Bippy)

    assert(wibble.bippy(42) === "42")
  }

}
